@Table(database = HideAppDB.class)
public class AppInfo extends BaseModel {
    @PrimaryKey
    @Column
    private String actName;
    @Column
    private String appPkg;
    @Column
    private String appLabel;
    @Column
    private boolean isHide;

    public AppInfo() {
    }

    public AppInfo(AppInfo other) {
        this.actName = other.getActName();
        this.appPkg = other.getAppPkg();
        this.appLabel = other.getAppLabel();
        this.isHide = other.isHide();
    }

    public AppInfo(String actName, String appPkg, String appLabel, boolean isHide) {
        this.actName = actName;
        this.appPkg = appPkg;
        this.appLabel = appLabel;
        this.isHide = isHide;
    }

    public boolean isHide() {
        return isHide;
    }

    public void setHide(boolean hide) {
        isHide = hide;
    }

    public String getAppLabel() {
        return appLabel;
    }

    public void setAppLabel(String appLabel) {
        this.appLabel = appLabel;
    }

    public String getAppPkg() {
        return appPkg;
    }

    public void setAppPkg(String appPkg) {
        this.appPkg = appPkg;
    }

    public String getActName() {
        return actName;
    }

    public void setActName(String appIcon) {
        this.actName = appIcon;
    }


}